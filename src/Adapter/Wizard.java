package Adapter;

public class Wizard {

    public void portal() {
        System.out.println("Wizard creates portal.");
    }

    public void shield() {
        System.out.println("Wizard creates shield.");

    }

    public void castDestructionSpell() {
        System.out.println("Wizard casted destruction spell.");


    }

    public void castProtectionSpell() {
        System.out.println("Wizard casted protection spell.");
    }

    public void justRun() {
        System.out.println("Wizard runs.");
    }

    public void pauseTime() {
        System.out.println("Wizard made the time stops");
    }

}
